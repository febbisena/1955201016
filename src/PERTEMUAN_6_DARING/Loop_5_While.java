public class Loop_5_While {
    public static void main(String[] args) {
        //Cetak Perulangan ke-0 sampai 10
        int i=0;
        
        while(i<=10){
            //blok kode yang akan diulang
            System.out.println("Perulangan ke-" +i);
            
            //Increment nilai i
            i++;
        }
    }
}
