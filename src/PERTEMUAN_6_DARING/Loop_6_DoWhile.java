public class Loop_6_DoWhile {
    public static void main(String[] args) {
        //Cetak Perulangan ke-0 sampai 10
        int i=0;
        
        do{
            System.out.println("Perulangan ke-" +i);
            i++;
        } while(i<=10);
    }
}
